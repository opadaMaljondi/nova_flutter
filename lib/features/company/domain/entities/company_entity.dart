class Company {
  final int id;
  final String name;
  final String description;
  final String image;
  final String location;
  final String phoneNumber;
  final String website;
  final String email;

  const Company({
    required this.id,
    required this.name,
    required this.description,
    required this.image,
    required this.location,
    required this.phoneNumber,
    required this.website,
    required this.email,
  });
}
